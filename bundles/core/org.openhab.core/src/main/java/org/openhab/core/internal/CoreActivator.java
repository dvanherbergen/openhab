package org.openhab.core.internal;

import org.openhab.core.threading.ThreadPoolService;
import org.osgi.framework.BundleActivator;
import org.osgi.framework.BundleContext;
import org.osgi.util.tracker.ServiceTracker;

/**
 * @author Davy Vanherbergen
 * 
 * @since 1.4.0
 */
public class CoreActivator implements BundleActivator {

	public static ServiceTracker<ThreadPoolService, ThreadPoolService> threadPoolServiceTracker;

	/*
	 * (non-Javadoc)
	 * 
	 * @see
	 * org.osgi.framework.BundleActivator#start(org.osgi.framework.BundleContext
	 * )
	 */
	public void start(BundleContext context) throws Exception {

		threadPoolServiceTracker = new ServiceTracker<ThreadPoolService, ThreadPoolService>(
				context, ThreadPoolService.class, null);
		threadPoolServiceTracker.open();
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see
	 * org.osgi.framework.BundleActivator#stop(org.osgi.framework.BundleContext)
	 */
	public void stop(BundleContext context) throws Exception {
		threadPoolServiceTracker.close();
	}

}
